print("Counting up from 0 to 50:")
for i in range(0, 51): #counts from 0-50 increment 1
    print(i)

print("\nCounting down from 50 to 0:")

for i in range(50, -1, -1): #counts down going 50 - 0 decrement -1
    print(i)

print("\nCounting up from 30 to 50:")

for i in range(30, 51): #starts counting from 30 - 50 increment 1
    print(i)

print("\nCounting down from 50 to 10 (step -2):") #starts counting down from 50 - 0 decrements of 2

for i in range(50, 9, -2):
    print(i)

print("\nCounting up from 100 to 200 (step +5):") #starts counting from 100 to 200 increments of 5

for i in range(100, 201, 5):
    print(i)

